function myFunctionSom(getal1, getal2) {
  var som = getal1 + getal2;
  document.getElementById("uitkomst").value = som;
}

function myFunctionMin(getal1, getal2) {
  var som = getal1 - getal2;
  document.getElementById("uitkomst").value = som;

}

function myFunctionMaal(getal1, getal2) {
  var som = getal1 * getal2;
  document.getElementById("uitkomst").value = som;

}

function myFunctionDelen(getal1, getal2) {
  var som = getal1 / getal2;
  document.getElementById("uitkomst").value = som;

}
